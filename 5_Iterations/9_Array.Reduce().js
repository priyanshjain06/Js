//REVIEW The reduce() method is used to reduce an array to a single value by running a function on each element of the array.

//REVIEW syntax :  array.reduce((accumulator, currentValue) => { ... }, initialValue)

// accumulator: the running total/result so far.

// currentValue: the current element in the loop.

// initialValue: the value to start the accumulation from.

const myNums = [1, 2, 3];

